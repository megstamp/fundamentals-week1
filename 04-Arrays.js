// IV. Arrays

// Arrays are lists. ORDER MATTERS!

// We use square brackets [] to denote an array.

const animals = ["lions", "tigers", "bears"]

// Arrays are "0-indexed" meaning the first element is element 0.

// We reference a single element by the name of the array then [] with the element number inside like:

console.log(animals[1]) // refers to "tigers"

// One important property of every array is the length.

console.log(animals.length) // 3

